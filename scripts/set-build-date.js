#!/usr/bin/env node
const { execSync } = require('child_process');
const { writeFileSync } = require('fs');
const { join } = require('path');

const MONTHS = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

function getCommitDate() {
  try {
    const iso = execSync('git log -1 --format=%cI', { encoding: 'utf8' }).trim();
    return new Date(iso);
  } catch {
    return new Date();
  }
}

const d = getCommitDate();
const formatted = `${d.getUTCDate()} ${MONTHS[d.getUTCMonth()]} ${d.getUTCFullYear()}`;

const out = `// Auto-generated by scripts/set-build-date.js — do not edit by hand.
export const BUILD_INFO = {
  date: '${formatted}',
};
`;

const target = join(__dirname, '..', 'src', 'environments', 'build-info.ts');
writeFileSync(target, out);
console.log(`build-info.ts updated: ${formatted}`);
